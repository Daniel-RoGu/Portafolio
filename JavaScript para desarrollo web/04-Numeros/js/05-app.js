//INCREMENTOS O DECREMENTOS

let puntaje = 4;
console.log(puntaje);

// incremento
puntaje++;
console.log(puntaje);

++puntaje;
console.log(puntaje);

puntaje+=5;
console.log(puntaje);



// decremento
puntaje--;
console.log(puntaje);

--puntaje;
console.log(puntaje);

puntaje-=5;
console.log(puntaje);